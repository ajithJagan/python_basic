import math 

print(math.sqrt(9)) # sqrt is used to get the square root of a number

print(math.ceil(1.2)) # ceil is used to round up the number

print(math.floor(1.2)) # floor is used to round down the number

print(math.pi) # pi is used to get the value of pi

print(math.pow(2,3)) # pow is used to get the power of a number

print(math.factorial(5)) # factorial is used to get the factorial of a number


